//Implementasikan rumus keliling dan luas dari Lingkaran dengan menggunakan Bahasa Dart

void main() {
  double pi = 3.14;
  int diameter = 28;
  double r = diameter/2;
  double luas = (pi*(r*r));
  double keliling = 2*pi*r;
 // L= π × r², dengan, π = konstanta pi (3.14 atau 22/7), dan r = jari-jari lingkaran.
print(luas);
// rumus keliling lingkaran adalah 2 x π x r
print(keliling);
}
